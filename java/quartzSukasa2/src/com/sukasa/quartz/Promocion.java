package com.sukasa.quartz;

/**
 * @author panchete
 *
 */
public class Promocion {
	
	int pcod;
	int pformapago;
	int ptipo;
	int pmes;
	double pfactor;
	/**
	 * @return the pcod
	 */
	public int getPcod() {
		return pcod;
	}
	/**
	 * @param pcod the pcod to set
	 */
	public void setPcod(int pcod) {
		this.pcod = pcod;
	}
	/**
	 * @return the pformapago
	 */
	public int getPformapago() {
		return pformapago;
	}
	/**
	 * @param pformapago the pformapago to set
	 */
	public void setPformapago(int pformapago) {
		this.pformapago = pformapago;
	}
	/**
	 * @return the ptipo
	 */
	public int getPtipo() {
		return ptipo;
	}
	/**
	 * @param ptipo the ptipo to set
	 */
	public void setPtipo(int ptipo) {
		this.ptipo = ptipo;
	}
	/**
	 * @return the pmes
	 */
	public int getPmes() {
		return pmes;
	}
	/**
	 * @param pmes the pmes to set
	 */
	public void setPmes(int pmes) {
		this.pmes = pmes;
	}
	/**
	 * @return the pfactor
	 */
	public double getPfactor() {
		return pfactor;
	}
	/**
	 * @param pfactor the pfactor to set
	 */
	public void setPfactor(double pfactor) {
		this.pfactor = pfactor;
	}
	

}
